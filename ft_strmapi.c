#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	len;
	unsigned int	i;
	char			*c;

	if (!s)
		return (0);
	i = 0;
	len = ft_strlen(s);
	c = malloc(sizeof(char) * (len + 1));
	if (!c)
		return (0);
	while (s[i])
	{
		c[i] = f(i, s[i]);
		i++;
	}
	c[i] = '\0';
	return (c);
}
