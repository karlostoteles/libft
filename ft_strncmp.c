#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && i < n && s1[i] != '\0')
		i++;
	if (n == i)
		return (0);
	return ((unsigned char)s1[i] - s2[i]);
}
